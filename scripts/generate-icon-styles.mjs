import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const CHECK_ONLY = process.argv.includes('--check');
const sourcePaths = [resolve(ROOT, 'src/app.ts'), resolve(ROOT, 'src/main.ts')];
const selectionPath = resolve(ROOT, 'node_modules/@phosphor-icons/web/src/regular/selection.json');
const outputPath = resolve(ROOT, 'src/generated/phosphor-icons.css');

const sources = await Promise.all(sourcePaths.map((path) => readFile(path, 'utf8')));
const iconNames = [
  ...new Set(sources.flatMap((source) => source.match(/ph-[a-z0-9-]+/gu) ?? [])),
].sort();
const selection = JSON.parse(await readFile(selectionPath, 'utf8'));
const codeByName = new Map(
  selection.icons.map((record) => [`ph-${record.properties.name}`, Number(record.properties.code)]),
);
const missing = iconNames.filter((name) => !codeByName.has(name));
if (missing.length > 0) {
  throw new Error(`unknown Phosphor icon names: ${missing.join(', ')}`);
}

const rules = iconNames.map((name) => {
  const code = codeByName.get(name);
  return `.${name}::before { content: "\\${code.toString(16).toUpperCase()}"; }`;
});
const content = `/* Generated by scripts/generate-icon-styles.mjs. Do not edit. */
@font-face {
  font-family: "Phosphor";
  src: url("@phosphor-icons/web/regular/Phosphor.woff2") format("woff2");
  font-style: normal;
  font-weight: normal;
  font-display: block;
}

.ph {
  display: inline-block;
  font-family: "Phosphor" !important;
  font-style: normal;
  font-weight: normal !important;
  font-variant: normal;
  line-height: 1;
  text-transform: none;
  speak: never;
  -webkit-font-smoothing: antialiased;
  -moz-osx-font-smoothing: grayscale;
}

${rules.join('\n')}
`;

if (CHECK_ONLY) {
  let existing = '';
  try {
    existing = await readFile(outputPath, 'utf8');
  } catch {
    // Reported as drift below.
  }
  if (existing !== content) {
    console.error(`icon style drift: ${outputPath}`);
    process.exitCode = 1;
  } else {
    console.log(`icon styles: ${String(iconNames.length)} icons, up to date`);
  }
} else {
  await writeFile(outputPath, content, 'utf8');
  console.log(`generated ${outputPath} with ${String(iconNames.length)} icons`);
}
