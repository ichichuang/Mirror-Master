import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const CHECK_ONLY = process.argv.includes('--check');
const sourcePath = resolve(ROOT, 'src/brand/brand.config.json');
const tsOutputPath = resolve(ROOT, 'src/brand/brand.config.ts');
const pythonOutputPath = resolve(ROOT, 'backend/app/generated_brand.py');
const config = JSON.parse(await readFile(sourcePath, 'utf8'));
const requiredKeys = ['productName', 'englishName', 'shortName', 'description', 'themeId'];

for (const key of requiredKeys) {
  if (typeof config[key] !== 'string' || config[key].trim().length === 0) {
    throw new Error(`brand config ${key} must be a non-empty string`);
  }
}

const normalized = Object.fromEntries(requiredKeys.map((key) => [key, config[key].trim()]));
const ts = `// Generated by scripts/generate-brand-config.mjs. Do not edit.
export const brandConfig = ${JSON.stringify(normalized, null, 2)} as const;
`;
const python = `"""Generated by scripts/generate-brand-config.mjs. Do not edit."""

BRAND_CONFIG = ${JSON.stringify(normalized, null, 2)}
PRODUCT_NAME = BRAND_CONFIG["productName"]
`;

if (CHECK_ONLY) {
  const [existingTs, existingPython] = await Promise.all([
    readOrEmpty(tsOutputPath),
    readOrEmpty(pythonOutputPath),
  ]);
  if (existingTs !== ts || existingPython !== python) {
    console.error('brand config drift: run pnpm generate:brand');
    process.exitCode = 1;
  } else {
    console.log(`brand config: ${normalized.productName}, up to date`);
  }
} else {
  await Promise.all([
    writeFile(tsOutputPath, ts, 'utf8'),
    writeFile(pythonOutputPath, python, 'utf8'),
  ]);
  console.log(`generated brand config for ${normalized.productName}`);
}

async function readOrEmpty(path) {
  try {
    return await readFile(path, 'utf8');
  } catch {
    return '';
  }
}
