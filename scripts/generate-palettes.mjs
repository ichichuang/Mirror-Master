import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const SOURCE_PATH = resolve(ROOT, '拼豆颜色对照表.txt');
const SOURCE_VERSION = '2026-07-24';
const CHECK_ONLY = process.argv.includes('--check');

const PALETTE_META = Object.freeze({
  default: Object.freeze({
    id: 'default',
    label: '默认色板',
    expectedCount: 39,
  }),
  mard: Object.freeze({
    id: 'mard',
    label: 'MARD',
    expectedCount: 221,
  }),
});

const sourceText = await readFile(SOURCE_PATH, 'utf8');
const palettes = parseOwnerSeed(sourceText);
validatePalettes(palettes);

const payload = Object.freeze({
  source: 'owner-seed',
  sourceFile: '拼豆颜色对照表.txt',
  sourceVersion: SOURCE_VERSION,
  palettes: Object.values(PALETTE_META).map((meta) => ({
    id: meta.id,
    label: meta.label,
    version: SOURCE_VERSION,
    colors: palettes[meta.id],
  })),
});

const outputs = new Map([
  [resolve(ROOT, 'data/palettes.generated.json'), `${JSON.stringify(payload, null, 2)}\n`],
  [resolve(ROOT, 'src/generated/palettes.ts'), renderTypeScript(payload)],
  [resolve(ROOT, 'backend/app/generated_palettes.py'), renderPython(payload)],
  [resolve(ROOT, 'docs/PALETTES.generated.md'), renderMarkdown(payload)],
]);

let hasDrift = false;

for (const [outputPath, content] of outputs) {
  if (CHECK_ONLY) {
    let existing = '';
    try {
      existing = await readFile(outputPath, 'utf8');
    } catch {
      // Missing output is reported as drift below.
    }

    if (existing !== content) {
      hasDrift = true;
      console.error(`palette asset drift: ${outputPath}`);
    }
    continue;
  }

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, content, 'utf8');
  console.log(`generated ${outputPath}`);
}

if (hasDrift) {
  process.exitCode = 1;
} else if (CHECK_ONLY) {
  console.log('palette assets: up to date');
}

function parseOwnerSeed(text) {
  const result = { default: [], mard: [] };
  let paletteId = null;

  for (const rawLine of text.split(/\r?\n/u)) {
    const line = rawLine.trim();

    if (line.startsWith('【默认色板 ')) {
      paletteId = 'default';
      continue;
    }
    if (line.startsWith('【MARD ')) {
      paletteId = 'mard';
      continue;
    }
    if (!paletteId || !/^[A-Z]\d+\s*=/u.test(line)) {
      continue;
    }

    const match = /^([A-Z]\d+)\s*=\s*(#[0-9A-F]{6})(?:\s+(.+))?$/u.exec(line);
    if (!match) {
      throw new Error(`invalid owner palette line: ${rawLine}`);
    }

    const [, code, displayHex, suppliedName] = match;
    const series = code[0];
    result[paletteId].push(
      Object.freeze({
        id: `${paletteId}:${code}`,
        source: 'owner-seed',
        version: SOURCE_VERSION,
        paletteId,
        series,
        code,
        displayHex,
        name: suppliedName?.trim() || null,
      }),
    );
  }

  return result;
}

function validatePalettes(palettesById) {
  const ids = new Set();

  for (const meta of Object.values(PALETTE_META)) {
    const colors = palettesById[meta.id];
    if (colors.length !== meta.expectedCount) {
      throw new Error(
        `${meta.label} count mismatch: expected ${String(meta.expectedCount)}, got ${String(colors.length)}`,
      );
    }

    for (const color of colors) {
      if (ids.has(color.id)) {
        throw new Error(`duplicate namespaced palette id: ${color.id}`);
      }
      if (color.series !== color.code[0]) {
        throw new Error(`palette series mismatch: ${color.id}`);
      }
      if (!/^#[0-9A-F]{6}$/u.test(color.displayHex)) {
        throw new Error(`invalid display HEX: ${color.id}`);
      }
      ids.add(color.id);
    }
  }

  if (ids.size !== 260) {
    throw new Error(`palette total mismatch: expected 260, got ${String(ids.size)}`);
  }
}

function renderTypeScript(data) {
  const colorRecords = data.palettes.flatMap((palette) => palette.colors);
  const paletteSummaries = data.palettes.map((palette) => ({
    id: palette.id,
    label: palette.label,
    version: palette.version,
    colorIds: palette.colors.map((color) => color.id),
  }));

  return `// Generated by scripts/generate-palettes.mjs. Do not edit.
export interface PaletteColorRecord {
  readonly id: string;
  readonly source: 'owner-seed';
  readonly version: string;
  readonly paletteId: 'default' | 'mard';
  readonly series: string;
  readonly code: string;
  readonly displayHex: string;
  readonly name: string | null;
}

export interface PaletteSummary {
  readonly id: 'default' | 'mard';
  readonly label: string;
  readonly version: string;
  readonly colorIds: readonly string[];
}

export const PALETTE_SOURCE_VERSION = ${JSON.stringify(data.sourceVersion)};
export const PALETTE_COLORS = Object.freeze(${JSON.stringify(colorRecords, null, 2)}) as readonly PaletteColorRecord[];
export const PALETTES = Object.freeze(${JSON.stringify(paletteSummaries, null, 2)}) as readonly PaletteSummary[];
`;
}

function renderPython(data) {
  const colorRecords = data.palettes.flatMap((palette) => palette.colors);
  const paletteSummaries = data.palettes.map((palette) => ({
    id: palette.id,
    label: palette.label,
    version: palette.version,
    colorIds: palette.colors.map((color) => color.id),
  }));
  const pythonJson = (value) =>
    JSON.stringify(value, null, 2)
      .replaceAll('null', 'None')
      .replaceAll('true', 'True')
      .replaceAll('false', 'False');

  return `"""Generated by scripts/generate-palettes.mjs. Do not edit."""

PALETTE_SOURCE_VERSION = ${JSON.stringify(data.sourceVersion)}
PALETTE_COLORS = ${pythonJson(colorRecords)}
PALETTES = ${pythonJson(paletteSummaries)}
`;
}

function renderMarkdown(data) {
  const lines = [
    '# 拼豆颜色清单（生成文件）',
    '',
    '> 非规范性生成物。产品要求以 [PRODUCT_SPEC.zh-CN.md](PRODUCT_SPEC.zh-CN.md) 为准；颜色值唯一来源为仓库根目录 `拼豆颜色对照表.txt`。',
    '',
    `Source version: \`${data.sourceVersion}\``,
    '',
  ];

  for (const palette of data.palettes) {
    lines.push(`## ${palette.label}（${String(palette.colors.length)} 色）`, '');
    lines.push('| ID | 系列 | Code | 显示 HEX | Seed 名称 |');
    lines.push('| --- | --- | --- | --- | --- |');
    for (const color of palette.colors) {
      lines.push(
        `| \`${color.id}\` | ${color.series} | ${color.code} | \`${color.displayHex}\` | ${color.name ?? '—'} |`,
      );
    }
    lines.push('');
  }

  return `${lines.join('\n').trimEnd()}\n`;
}
