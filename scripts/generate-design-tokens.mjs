import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const CHECK_ONLY = process.argv.includes('--check');
const brandConfig = JSON.parse(
  await readFile(resolve(ROOT, 'src/brand/brand.config.json'), 'utf8'),
);
const ACTIVE_THEME = brandConfig.themeId;
if (typeof ACTIVE_THEME !== 'string' || ACTIVE_THEME.trim().length === 0) {
  throw new Error('brand config themeId must be a non-empty string');
}
const inputPaths = [
  resolve(ROOT, 'src/design/tokens/core.tokens.json'),
  resolve(ROOT, `src/design/tokens/themes/${ACTIVE_THEME}.tokens.json`),
  resolve(ROOT, 'src/design/tokens/semantic.tokens.json'),
  resolve(ROOT, 'src/design/tokens/component.tokens.json'),
];
const cssOutputPath = resolve(ROOT, 'src/design/generated/tokens.css');
const tsOutputPath = resolve(ROOT, 'src/design/generated/tokens.ts');

const documents = await Promise.all(
  inputPaths.map(async (path) => JSON.parse(await readFile(path, 'utf8'))),
);
const tokens = new Map();
for (const document of documents) {
  flattenTokens(document, [], tokens);
}

const resolved = new Map(
  [...tokens.keys()].map((path) => [path, resolveToken(path, tokens, new Set())]),
);
const cssVariables = [...resolved.entries()]
  .filter(([path]) => !path.startsWith('theme.'))
  .map(([path, value]) => `  --${toKebab(path)}: ${value};`);
const legacyAliases = {
  'color-app': 'color-background-page',
  'color-surface': 'color-background-panel',
  'color-surface-raised': 'color-background-panel',
  'color-canvas-well': 'canvas-background',
  'color-ink': 'color-text-primary',
  'color-ink-muted': 'color-text-secondary',
  'color-border': 'color-border-default',
  'color-primary': 'color-action-primary',
  'color-primary-strong': 'color-action-primary-hover',
  'color-primary-soft': 'color-action-primary-soft',
  'color-focus': 'color-focus-ring',
  'color-danger': 'color-status-error',
  'color-warning': 'color-status-warning',
  'color-page': 'color-background-page',
  'color-text': 'color-text-primary',
  'color-heading': 'color-text-primary',
  'color-muted': 'color-text-secondary',
  'color-accent': 'color-action-primary',
  'color-accent-strong': 'color-action-primary-hover',
  'color-selection': 'color-action-primary',
  'color-selection-text': 'color-text-on-primary',
  'color-code-bg': 'color-background-subtle',
  'color-code-border': 'color-border-default',
  'color-code-text': 'color-text-primary',
  'radius-small': 'radius-sm',
  'radius-medium': 'radius-md',
  'radius-large': 'radius-lg',
  'shadow-sheet': 'shadow-panel',
  'header-height': 'layout-header-height',
  'safe-bottom': 'layout-safe-bottom',
};
const aliasVariables = Object.entries(legacyAliases).map(
  ([alias, target]) => `  --${alias}: var(--${target});`,
);
const css = `/* Generated by scripts/generate-design-tokens.mjs. Do not edit. */
:root,
[data-theme="${ACTIVE_THEME}"] {
  color-scheme: light;
${[...cssVariables, ...aliasVariables].join('\n')}
}
`;
const ts = `// Generated by scripts/generate-design-tokens.mjs. Do not edit.
export const ACTIVE_THEME = ${JSON.stringify(ACTIVE_THEME)} as const;
export const DESIGN_TOKENS = Object.freeze(${JSON.stringify(Object.fromEntries(resolved), null, 2)}) as Readonly<Record<string, string>>;
`;

if (CHECK_ONLY) {
  const [existingCss, existingTs] = await Promise.all([
    readOrEmpty(cssOutputPath),
    readOrEmpty(tsOutputPath),
  ]);
  if (existingCss !== css || existingTs !== ts) {
    console.error('design token drift: run pnpm generate:tokens');
    process.exitCode = 1;
  } else {
    console.log(`design tokens: ${String(resolved.size)} tokens, up to date`);
  }
} else {
  await mkdir(dirname(cssOutputPath), { recursive: true });
  await Promise.all([writeFile(cssOutputPath, css, 'utf8'), writeFile(tsOutputPath, ts, 'utf8')]);
  console.log(`generated design tokens for ${ACTIVE_THEME}`);
}

function flattenTokens(value, path, target) {
  if (value && typeof value === 'object' && '$value' in value) {
    target.set(path.join('.'), String(value.$value));
    return;
  }
  if (!value || typeof value !== 'object' || Array.isArray(value)) {
    throw new Error(`invalid token group at ${path.join('.') || '<root>'}`);
  }
  for (const [key, child] of Object.entries(value)) {
    if (!key.startsWith('$')) {
      flattenTokens(child, [...path, key], target);
    }
  }
}

function resolveToken(path, source, resolving) {
  const value = source.get(path);
  if (value === undefined) {
    throw new Error(`unknown token reference: ${path}`);
  }
  if (resolving.has(path)) {
    throw new Error(`circular token reference: ${[...resolving, path].join(' -> ')}`);
  }
  const match = value.match(/^\{([^}]+)\}$/u);
  if (!match) {
    return value;
  }
  resolving.add(path);
  const result = resolveToken(match[1], source, resolving);
  resolving.delete(path);
  return result;
}

function toKebab(path) {
  return path
    .replaceAll('.', '-')
    .replace(/([a-z0-9])([A-Z])/gu, '$1-$2')
    .toLowerCase();
}

async function readOrEmpty(path) {
  try {
    return await readFile(path, 'utf8');
  } catch {
    return '';
  }
}
